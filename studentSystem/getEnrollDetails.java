package studentSystem;

public class getEnrollDetails {
	public static String Term, Year, Dept, SemId;
	
	public static void setTerm(String term){
		Term=term;
	}
	
	public static String getTerm(){
		return Term;
	}
	
	public static void setYear(String year){
		Year=year;
	}
	
	public static String getYear(){
		return Year;
	}
	
	public static void setDept(String dept){
		Dept=dept;
	}
	
	public static String getDept(){
		return Dept;
	}
	
	public static void setSemId(String semId){
		SemId=semId;
	}
	
	public static String getSemId(){
		return SemId;
	}
}
